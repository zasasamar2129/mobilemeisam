export type LanguageType = 'fa' | 'en';
export type TranslationTextType = typeof translations.fa;
export function getTranslation(lang: 'fa' | 'en'): TranslationTextType {
  return translations[lang];
}
export function formatCurrency(amount: number, lang: 'fa' | 'en'): string {
  if (lang === 'fa') {
    return `${Number(amount).toLocaleString('fa-IR')} ریال`;
  }
  return `${Number(amount).toLocaleString('en-US')} IRR`;
}
